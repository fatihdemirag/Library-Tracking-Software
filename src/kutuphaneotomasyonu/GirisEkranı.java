    private void girisButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_girisButtonActionPerformed
        try {
            SqlBaglantisi baglanti = new SqlBaglantisi();
            baglanti.Baglan(new SqlBaglantisi().getDbUrl(), new SqlBaglantisi().getdbKullanici(), new SqlBaglantisi().getdbSifre());
            SqlBaglantisi.prs = SqlBaglantisi.con.prepareStatement("select * from personel where personel_no='" + kullaniciAdiTextBox.getText() + "' and personel_sifre='" + sifreTextBox.getText() + "'");
            SqlBaglantisi.rs = SqlBaglantisi.prs.executeQuery();

            if (SqlBaglantisi.rs.next()) {
                Anasayfa anasayfa = new Anasayfa();
                anasayfa.setVisible(true);

                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Yanlış veya Eksik Bilgi Girdiniz Lütfen Bilgileri Kontrol Ediniz !", "Bilgileri Kontrol Ediniz", HEIGHT);
            }

            while (SqlBaglantisi.rs.next()) {

                personel.setAdi(SqlBaglantisi.rs.getString("ad"));
                personel.setSoyadi(SqlBaglantisi.rs.getString("soyad"));
                personel.setPersonelNo(SqlBaglantisi.rs.getInt("personel_no"));

            }
            PersonelYazdir();
            Log(true, kullaniciAdiTextBox.getText());

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Giriş Yapılamadı");
            try {
                Log(false, kullaniciAdiTextBox.getText());
            } catch (IOException ex) {
                Logger.getLogger(GirisEkranı.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_girisButtonActionPerformed

    private void sifreTextBoxKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_sifreTextBoxKeyTyped
        if (evt.getKeyChar() == KeyEvent.VK_ENTER) {
            girisButton.requestFocus();
        }
        if (evt.getKeyChar() == KeyEvent.VK_SPACE || sifreTextBox.getText().length() == 25) {
            evt.consume();
        }
    }//GEN-LAST:event_sifreTextBoxKeyTyped

