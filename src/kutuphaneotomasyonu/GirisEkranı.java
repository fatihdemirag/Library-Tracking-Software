    private void PersonelYazdir() throws IOException {
        File personelNumarasi = new File("personel_no.txt");
        if (personelNumarasi.exists()) {
            personelNumarasi.createNewFile();
        }
        FileWriter yazdir = new FileWriter(personelNumarasi, false);
        //Buffer veriler belleğe yazılmadan önce bir yerde toplanıp belleğe yazma işlemi ondan sonra yapılır.
        BufferedWriter buffer = new BufferedWriter(yazdir);
        buffer.write(kullaniciAdiTextBox.getText());
        buffer.close();

    }

    private void Log(boolean b, String personelNo) throws IOException {
        File log = new File("log.txt");
        if (log.exists()) {
            log.createNewFile();
        }
        FileWriter yazdir = new FileWriter(log, true);
        BufferedWriter buffer = new BufferedWriter(yazdir);
        if (b) {
            buffer.write("Başarılı Giriş" + " - [Personel : " + personelNo + " ] - [" + c.getTime() + "]" + "]\n");
        } else {
            buffer.write("Başarısız Giriş" + " - [Personel : " + personelNo + " ] - [" + c.getTime() + "]" + "]\n");
        }
        buffer.close();

    }
